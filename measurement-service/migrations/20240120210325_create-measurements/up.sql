CREATE TABLE air_measurements (
    measure_time TIMESTAMP NOT NULL,
    left_temperature DOUBLE PRECISION NULL,
    left_humidity DOUBLE PRECISION NULL,    
    left_pressure DOUBLE PRECISION NULL,    
    left_resistance DOUBLE PRECISION NULL,    
    right_temperature DOUBLE PRECISION NULL,
    right_humidity DOUBLE PRECISION NULL,    
    right_pressure DOUBLE PRECISION NULL,    
    right_resistance DOUBLE PRECISION NULL,    
)
